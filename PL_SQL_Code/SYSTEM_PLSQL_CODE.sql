--DCL(GRANT, REVOKE)
--GRANT: USER나 OBJECT에서 권한을 부여할 때 사용
--REVOKE: USER나 OBJECT에서 권한을 회수할 때 사용

CREATE USER TEST
IDENTIFIED BY TEST
DEFAULT TABLESPACE USERS
TEMPORARY TABLESPACE TEMP
QUOTA 1M ON USERS;

CREATE USER INSA
IDENTIFIED BY INSA
DEFAULT TABLESPACE USERS
TEMPORARY TABLESPACE TEMP
QUOTA 1M ON USERS;

SELECT * FROM DBA_USERS;

SELECT * FROM V$SESSION
WHERE USERNAME IS NOT NULL;


--권한의 종류
SELECT * FROM SYSTEM_PRIVILEGE_MAP ORDER BY NAME;

--CREATE TABLE: 내 테이블만 생성 가능
--CREATE ANY TABLE: 다른 계정의 테이블도 생성가능

--SYSTEM권한: DATABASE의 OBJECT들을 생성(CREATE), 수정(ALTER), 삭제(DROP) 할 수 있는 권한
--				DBA(관리자) -> USERS(사용자)
--OBJECT권한: USER가 만든 OBJECT를 조작(INSERT, UPDATE, DELETE, SELECT)할 수 있는 권한

--OBJECT: TABLE, ROLE, SEQEUNCE, VIEW, SYNONYM

--SYSTEM권한 주기
GRANT CREATE USER TO KIM;
GRANT CREATE VIEW TO KIM;

--SYNONYM은 다른 스키마에 별명을 붙여준다고 보면 됨.
GRANT CREATE SESSION, CREATE SYNONYM TO TEST;
