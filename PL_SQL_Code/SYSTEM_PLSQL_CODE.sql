--ROLE(역할)
--CONNECT, RESOURCE
SELECT * FROM ROLE_SYS_PRIVS WHERE ROLE IN('CONNECT', 'RESOURCE', 'MANAGER') ORDER BY ROLE;

CREATE ROLE MANAGER;

SELECT * FROM DBA_ROLES;

GRANT CREATE SESSION, CREATE TABLE, CREATE USER TO MANAGER;

GRANT MANAGER TO INSA;

REVOKE MANAGER FROM INSA;
--ROLE의 GRANT나 REVOKE 재접속 후 적용 됨

GRANT CREATE ROLE TO KIM;





































